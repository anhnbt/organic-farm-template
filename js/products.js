function chonSP() {
	var arr1 = document.getElementsByClassName('category');
	var category_arr = [];
	for (i = 0; i < arr1.length; i++) {
		if (arr1[i].checked == true) category_arr.push(arr1[i].value);
	}
	showSP(category_arr);
}
